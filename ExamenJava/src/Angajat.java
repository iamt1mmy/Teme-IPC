
public class Angajat{

	private String salariu;
	private String nume;
	private int anVechime;
	public Angajat(String salariu, String nume, int anVechime) {
		this.salariu = salariu;
		this.nume = nume;
		this.anVechime = anVechime;
	}
	public String getSalariu() {
		return salariu;
	}
	public void setSalariu(String salariu) {
		this.salariu = salariu;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}
	public int getAnVechime() {
		return anVechime;
	}
	public void setAnVechime(int anVechime) {
		this.anVechime = anVechime;
	}
	
	
}
